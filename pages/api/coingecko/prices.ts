import type { NextApiRequest, NextApiResponse } from 'next';
import { coingeckoAxiosClient } from '.';

export interface CoinGeckoCoinPriceApiData {
  id: string;
  symbol: string;
  name: string;
  image: string;
  current_price: number;
  market_cap: number;
  market_cap_rank: number;
  fully_diluted_valuation: number;
  total_volume: number;
  high_24h: number;
  low_24h: number;
  price_change_24h: number;
  price_change_percentage_24h: number;
  market_cap_change_24h: number;
  market_cap_change_percentage_24h: number;
  circulating_supply: number;
  total_supply: number;
  max_supply: number;
  ath: number;
  ath_change_percentage: number;
  ath_date: string;
  atl: number;
  atl_change_percentage: number;
  atl_date: string;
  roi: null;
  last_updated: string;
}

const handler = async (req: NextApiRequest, res: NextApiResponse) => {
  const response = await coingeckoAxiosClient
    .get<readonly CoinGeckoCoinPriceApiData[]>('/coins/markets', { params: { ...req.query, vs_currency: 'USD' } })
    .catch((err) => {
      return { status: err.response?.status, data: err.response?.data };
    });

  res.status(response.status ?? 500).json(response.data);
};

export default handler;
